export function transliterate(str: string): string {
    if (!str) return ''
    return str.replace(/./g, (char) => {
        if (char.match(/[^a-zA-Zа-яА-Я\d\s-]/)) {
            return ''
        }
        if (char.match(/[а-яА-Я]/)) {
            if (char.match(/[ъЪьЬ]/)) {
                return ''
            }
            return ruEnMap[char] || char
        } else if (char.match(/\s/)) {
            if (str.trim().slice(-1) !== '-') {
                return '-'
            } else {
                return ''
            }
        } else {
            return char
        }
    })
}
